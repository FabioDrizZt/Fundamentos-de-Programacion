// operador de asignación =
let a = 10

// operador de asignacion e incremento +=

a += 10
console.log({ a });

// operador de asignacion e decremento -=
a -= 10
console.log({ a });

// operador de asignacion e multiplicación *=
a *= 10
console.log({ a });

// operador de asignacion e división /=
a /= 10
console.log({ a });

// operador de asignación e módulo %=
a %= 10
console.log({ a });